package com.neotech.lesson15;

public class ReturnCalculator {

	// return statement
	int add(int a, int b) {
		int sum = a + b;
		return sum; // add nums and send the sum back
	}

	////////////////////////////////////////////////////
	int multiply(int a, int b) {
		return a * b; // send back the product
	}

	////////////////////////////////////////////////////
	double divide(double a, double b) {
		return a / b;
		// OR
		// double res = a/b;
		// return res;

	}

}
