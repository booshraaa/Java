package com.neotech.lesson08;

public class IncrementAndDecrement {
	public static void main(String[] args) {
		
		//declaring
		int x = 10;
		
		//re-assigning
		x = 11;     
		
		//shorthand
		x += 1;    
		
		//increment
		x ++;
		
		System.out.println(x);
		
		////////////////////////////////////////////
		
		//declaring
		int y = 50;
		
		//assigning
		y = y-1;
		
		//Shorthand
		y -= 1;
		
		//Decrement
		y --;
		
		System.out.println(y);
		
		
		
		
		
	}

}
